package sk.upjs.ics.diplomovka.absolutechromosome;

public class FlightPosition {

    private int stand;
    private int flight;

    public FlightPosition(int stand, int flight) {
        this.stand = stand;
        this.flight = flight;
    }

    public int getStand() {
        return stand;
    }

    public int getFlight() {
        return flight;
    }
}
